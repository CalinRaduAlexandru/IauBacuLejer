import React from "react";

import FormInput from "../FormInput/FormInput.jsx";
import Btn from "../../Ingredients/Btn/Btn.jsx";
import FacebookBtn from "../../Ingredients/Btn/FacebookBtn.jsx";

import { auth, createUserProfileDocument } from "../../utils/init-firebase";

import "./SignUp.scss";

class SignUp extends React.Component {
  constructor() {
    super();

    this.state = {
      displayName: "",
      email: "",
      password: "",
      confirmPassword: "",
    };
  }

  handleSubmit = async (event) => {
    event.preventDefault();

    const { displayName, email, password, confirmPassword } = this.state;

    if (password !== confirmPassword) {
      alert("passwords don't match");
      return;
    }

    try {
      const { user } = await auth.createUserWithEmailAndPassword(
        email,
        password
      );

      await createUserProfileDocument(user, { displayName });

      this.setState({
        displayName: "",
        email: "",
        password: "",
        confirmPassword: "",
      });
    } catch (error) {
      console.error(error);
    }
  };

  handleChange = (event) => {
    const { name, value } = event.target;

    this.setState({ [name]: value });
  };

  render() {
    const { displayName, email, password, confirmPassword } = this.state;
    return (
      <div className="sign-up">
        <span className="exit">&#x2718;</span>
        <h2 className="title">Înregistrează-te</h2>

        <form className="sign-up-form" onSubmit={this.handleSubmit}>
          <FormInput
            type="text"
            name="displayName"
            value={displayName}
            onChange={this.handleChange}
            label="Display Name"
            required
          />
          <FormInput
            type="email"
            name="email"
            value={email}
            onChange={this.handleChange}
            label="Email"
            required
          />
          <FormInput
            type="password"
            name="password"
            value={password}
            onChange={this.handleChange}
            label="Password"
            required
          />
          <FormInput
            type="password"
            name="confirmPassword"
            value={confirmPassword}
            onChange={this.handleChange}
            label="Confirm Password"
            required
          />
          <div className="btns-group">
            <Btn type="submit" buttonStyle={"btn--secondary--ghost"}>
              Înregistrează-te
            </Btn>
            <span>Sau</span>
            <FacebookBtn>Continuă cu Facebook</FacebookBtn>
          </div>
        </form>
      </div>
    );
  }
}

export default SignUp;
